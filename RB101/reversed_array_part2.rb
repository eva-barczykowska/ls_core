Data Structures
input: array
output: new array but elements are in reverse order

Algorithm
-initialize an empty array
-iterate through the input array
-how to take an element (last element of the array for example) in a non-mutating way (Array#last)
-append this element to the result array
-do this until you iterate through the whole array
-return the result

